import { Schema, model } from 'mongoose';

const invoiceSchema = new Schema({
  patient: {
    type: Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  
  products: [{
    product: {
      type: Schema.Types.ObjectId,
      ref: 'Product',
      required: true
    },
    quantity: {
      type: Number,
      required: true
    },
    
    price: {
      type: Number,
      required: true
    }
  }],
  status: {
    type: Boolean,
    default: true
  },
  total_amount: {
    type: Number,
    required: true
  },
  created_at: {
    type: Date,
    default: Date.now
  },
  updated_at: {
    type: Date,
    default: Date.now
  }
});

const Invoice = model('Invoice', invoiceSchema);

export default Invoice;
